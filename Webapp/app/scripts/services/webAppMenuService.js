	// 过滤掉不被允许的菜单
	var filterAllowMenus = function (tree, menus) {
		angular.forEach(tree, function (menu, key) {
			if (menus.indexOf(menu.id) === -1) {
				tree[key].show = false;
			} else if (tree._children && tree._children.length > 0) {
				filterAllowMenus(menu, menus);
			}
		});
	};

